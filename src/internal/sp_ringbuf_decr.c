#include "../internal.h"
#include <stdlib.h>

void sp_ringbuf_decr(void **ptr, const void *buf, size_t capacity, size_t elem_size)
{
	if (*ptr == buf)
		*ptr = (char*)buf + (capacity - 1) * elem_size;
	else
		*ptr = (char*)(*ptr) - elem_size;
}
